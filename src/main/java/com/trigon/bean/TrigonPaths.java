package com.trigon.bean;


public class TrigonPaths {

    private String testResultsPath;
    private String detailedReportPath;
    private String supportFilePath;
    private String logsPath;
    private String supportSubSuiteFilePath;
    private String supportFileHTMLPath;
    private String screenShotsPath;

    public String getTestResultsPath() {
        return testResultsPath;
    }

    public void setTestResultsPath(String testResultsPath) {
        this.testResultsPath = testResultsPath;
    }

    public String getDetailedReportPath() {
        return detailedReportPath;
    }

    public void setDetailedReportPath(String detailedReportPath) {
        this.detailedReportPath = detailedReportPath;
    }

    public String getSupportFilePath() {
        return supportFilePath;
    }

    public void setSupportFilePath(String supportFilePath) {
        this.supportFilePath = supportFilePath;
    }

    public String getLogsPath() {
        return logsPath;
    }

    public void setLogsPath(String logsPath) {
        this.logsPath = logsPath;
    }

    public String getSupportSubSuiteFilePath() {
        return supportSubSuiteFilePath;
    }

    public void setSupportSubSuiteFilePath(String supportSubSuiteFilePath) {
        this.supportSubSuiteFilePath = supportSubSuiteFilePath;
    }

    public String getSupportFileHTMLPath() {
        return supportFileHTMLPath;
    }

    public void setSupportFileHTMLPath(String supportFileHTMLPath) {
        this.supportFileHTMLPath = supportFileHTMLPath;
    }

    public String getScreenShotsPath() {
        return screenShotsPath;
    }

    public void setScreenShotsPath(String screenShotsPath) {
        this.screenShotsPath = screenShotsPath;
    }
}


